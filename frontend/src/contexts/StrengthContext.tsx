// 실제 API 응답 구조
type ApiResponse = {
    success: boolean;
    message: string;
    data: {
        [categoryName: string]: ApiKeywordInfo[];
    };
    errorCode: string | null;
}

export class CoinInformations {
    /**
     * 씹망할 데이터가 성공적으로 로딩되었다면 true, 아니면 씨발 false를 가집니다.
     */

    // 카테고리별 코인 정보 저장
    public categories: Map<string, CoinType> = new Map();
    
    public constructor(rawData: ApiResponse) {
        if (!this.validateRawData(rawData)) throw new Error("Validation Failed");

        const { data } = rawData;

        // 각 카테고리별로 데이터 변환
        for (const [categoryName, keywordsList] of Object.entries(data)) {
            if (keywordsList.length > 0) {
                // 첫 번째 키워드의 코인 정보를 사용 (같은 카테고리의 모든 키워드는 같은 코인을 가짐)
                const coinInfo = keywordsList[0].coin;
                
                const coinType: CoinType = {
                    id: coinInfo.id,
                    name: coinInfo.name,
                    description: coinInfo.description,
                    keywords: keywordsList.map((keyword: ApiKeywordInfo) => ({
                        name: keyword.name,
                        description: keyword.description,
                        id: keyword.id
                    }))
                };
                
                this.categories.set(categoryName, coinType);
            }
        }
    }

    // 기존 속성들과의 호환성을 위한 getter들
    public get growth(): CoinType {
        return this.categories.get('관리와 성장')!;
    }

    public get emotion(): CoinType {
        return this.categories.get('감정과 태도')!;
    }

    public get creativity(): CoinType {
        return this.categories.get('창의와 몰입')!;
    }

    public get problemSolving(): CoinType {
        return this.categories.get('사고와 해결')!;
    }

    public get relationship(): CoinType {
        return this.categories.get('관계와 공감')!;
    }

    public get belief(): CoinType {
        return this.categories.get('신념과 실행')!;
    }

    // 모든 카테고리명 반환
    public getCategoryNames(): string[] {
        return Array.from(this.categories.keys());
    }

    // 특정 카테고리의 코인 정보 반환
    public getCoinByCategory(categoryName: string): CoinType | undefined {
        return this.categories.get(categoryName);
    }

    private validateRawData(rawData: ApiResponse): boolean {
        if (!rawData || typeof rawData !== 'object') return false;
        
        if (!('success' in rawData && 'message' in rawData && 'data' in rawData)) {
            return false;
        }

        if (!rawData.data || typeof rawData.data !== 'object') return false;

        // 각 카테고리의 키워드 배열 검증
        for (const [, keywordsList] of Object.entries(rawData.data)) {
            if (!Array.isArray(keywordsList)) return false;

            for (const keyword of keywordsList) {
                if (!(
                    'name' in keyword &&
                    'description' in keyword &&
                    'id' in keyword &&
                    'coin' in keyword &&
                    'coinName' in keyword
                )) return false;

                // coin 객체 검증
                const coin = keyword.coin;
                if (!(
                    'id' in coin &&
                    'name' in coin &&
                    'description' in coin &&
                    'color' in coin
                )) return false;
            }
        }

        return true;
    }
}


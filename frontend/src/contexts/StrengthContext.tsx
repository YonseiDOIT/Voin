// 실제 API 응답에서의 코인 정보 구조
type ApiCoinInfo = {
    createdAt: string;
    updatedAt: string;
    id: number;
    name: string;
    description: string;
    color: string;
    fullInfo: string;
}

// 실제 API 응답에서의 키워드 정보 구조
type ApiKeywordInfo = {
    createdAt: string;
    updatedAt: string;
    id: number;
    coin: ApiCoinInfo;
    name: string;
    description: string;
    coinName: string;
    fullInfo: string;
}

// 내부에서 사용할 키워드 타입
type CoinKeywordType = {
    /**
     * 이 망할 요소는 사용자에게 보여질 실제 키워드 이름입니다.
     */
    name: string;

    /**
     * 이 씨바랄 요소는 키워드에 대한 설명입니다.
     */
    description: string;

    /**
     * 이 개버러지같은 요소는 모든 씹코인에 대해서 중복이 안됩니다.
     */
    id: number;
}

type CoinType = {
    /**
     * 이 씨바랄 요소는 개씨발 중복이 안됩니다.
     */
    id: number;

    /**
     * 이 망할 요소는 실제로 보여지는 망할 이름입니다.
     */
    name: string;

    /**
     * 거지같은 name 요소와 동일합니다.
     */
    description: string;


    /**
     * 이 지랄난 요소는 해당 코인이 가진 망할 키워드들을 저장하고 있습니다.
     */
    keywords: CoinKeywordType[];
};

